+function ($) { "use strict";

  // POPOVER PUBLIC CLASS DEFINITION
  // ===============================

  var Popover = function (element, options) {
    this.init('popover', element, options)
  }

  if (!$.fn.tooltip) throw new Error('Popover requires tooltip.js')

  Popover.DEFAULTS = $.extend({} , $.fn.tooltip.Constructor.DEFAULTS, {
    placement: 'right'
  , trigger: 'click'
  , content: ''
  , template: '<div class="popover"><div class="arrow"></div><h3 class="popover-title"></h3><div class="popover-content"></div></div>'
  })


  // NOTE: POPOVER EXTENDS tooltip.js
  // ================================

  Popover.prototype = $.extend({}, $.fn.tooltip.Constructor.prototype)

  Popover.prototype.constructor = Popover

  Popover.prototype.getDefaults = function () {
    return Popover.DEFAULTS
  }

  Popover.prototype.setContent = function () {
    var $tip    = this.tip()
    var title   = this.getTitle()
    var content = this.getContent()

    $tip.find('.popover-title')[this.options.html ? 'html' : 'text'](title)
    $tip.find('.popover-content')[this.options.html ? 'html' : 'text'](content)

    $tip.removeClass('fade top bottom left right in')

    // IE8 doesn't accept hiding via the `:empty` pseudo selector, we have to do
    // this manually by checking the contents.
    if (!$tip.find('.popover-title').html()) $tip.find('.popover-title').hide()
  }

  Popover.prototype.hasContent = function () {
    return this.getTitle() || this.getContent()
  }

  Popover.prototype.getContent = function () {
    var $e = this.$element
    var o  = this.options

    return $e.attr('data-content')
      || (typeof o.content == 'function' ?
            o.content.call($e[0]) :
            o.content)
  }

  Popover.prototype.arrow = function () {
    return this.$arrow = this.$arrow || this.tip().find('.arrow')
  }

  Popover.prototype.tip = function () {
    if (!this.$tip) this.$tip = $(this.options.template)
    return this.$tip
  }


  // POPOVER PLUGIN DEFINITION
  // =========================

  var old = $.fn.popover

  $.fn.popover = function (option) {
    return this.each(function () {
      var $this   = $(this)
      var data    = $this.data('bs.popover')
      var options = typeof option == 'object' && option

      if (!data) $this.data('bs.popover', (data = new Popover(this, options)))
      if (typeof option == 'string') data[option]()
    })
  }

  $.fn.popover.Constructor = Popover


  // POPOVER NO CONFLICT
  // ===================

  $.fn.popover.noConflict = function () {
    $.fn.popover = old
    return this
  }

}(jQuery);

(function($){
  $.fn.extend({
    //通用ajax表单提交验证方法，最大程度兼容html5 form表单提交
    //通过jquery选择器获取需要验证的表单元素数组，只针对input、textarea标签
    //通过标签的required属性判断元素是否为必填(必选)，优先级高于pattern
    //通过标签的pattern属性获取验证的正则表达式，然后进行验证
    //通过标签的data-group属性获取组，同一组内的所有值应相同，组名以字母开头，应用场景：两次输入密码一致
    validateForm5: function (es){
      var result = true;
      var tags = this;
      
      //popover模版
      var popoverOptions = {
        placement: "right",
        trigger: "focus",
        content: "",
        container: "body"
      };
      
      //默认错误提示模版
      var errors = {
        //必要
        "required":{
          //必要-必填
          "needValue": "此项不可以为空",
          //必要-必选
          "needCheck": "此项必选"
        },
        //格式
        "pattern":"格式不正确",
        //一致性
        "group":"与上一次输入不一致"
      };
      //导入用户模版
      errors = es || errors;
      
      //遍历-移除所有错误样式、销毁提示框
      for(var i=0;i<tags.length;i++){
        $(tags[i]).removeClass("has-error-extend");
        $(tags[i]).popover("destroy");
      }
      
      //遍历-验证必填(必选)项
      for(var i=0;i<tags.length;i++){
        var s = $(tags[i]);
        //判断是否有required属性
        if(s.attr("required")){
          //判断是否为checkbox或radio元素
          if(s.attr("type") == "checkbox" || s.attr("type") == "radio"){
            //判断是否选中
            if(!s.attr("checked")){
              //没有选中
              //添加错误样式
              s.addClass("has-error-extend");
              //添加错误提示框
              popoverOptions.content = errors.required.needCheck;
              s.popover(popoverOptions);
              //结果标记为false
              result = false;
            }
          }else{
            //判断是否有值
            if(s.val() == ""){
              //没有值
              //添加错误样式
              s.addClass("has-error-extend");
              //添加错误提示框
              popoverOptions.content = errors.required.needValue;
              s.popover(popoverOptions);
              //结果标记为false
              result = false;
            }
          }
        }
      }
      
      //遍历-验证表达式
      for(var i=0;i<tags.length;i++){
        var s = $(tags[i]);
        //获取验证表达式
        var regText = s.attr("pattern");
        //判断是否有验证表达式
        if(regText){
          //有表达式，则创建正则对象，进行验证(忽略为空的情况)
          var reg = new RegExp(regText);
          if(!reg.test(s.val()) && s.val()!=""){
            //验证不通过
            //添加错误样式
            s.addClass("has-error-extend");
            //添加错误提示框
            popoverOptions.content = errors.pattern;
            s.popover(popoverOptions);
            //结果标记为false
            result = false;
          }
        }
      }
      
      //遍历-验证组
      var group = {};
      for(var i=0;i<tags.length;i++){
        var s = $(tags[i]);
        //判断有无组属性
        var groupName = s.attr("data-group");
        if(groupName){
          //判断属性值是否存过
          var groupValue = group[groupName];
          if(groupValue){
            //保存过判断当前值与保存的值是否相等
            if(groupValue != s.val()){
              //不相同
              //添加错误样式
              s.addClass("has-error-extend");
              //添加错误提示框
							popoverOptions.content = errors.group;
							s.popover(popoverOptions);
              //结果标记为false
              result = false;
            }
          }else{
            //没存过直接保存
            group[groupName] = s.val();
          }
        }
      }

      return result;
    }
  });
})(jQuery);